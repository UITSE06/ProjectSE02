    private void LoadData() {
        dtm = (DefaultTableModel) tblListTypeOfCourse.getModel();
        //xoa tat ca cac dong hien tai neu co
        if (dtm.getRowCount() > 0) {
            for (int i = (dtm.getRowCount() - 1); i >= 0; i--) {
                dtm.removeRow(i);
            }
        }
        try {
            ResultSet rs = tcBLL.LoadTypeOfCourse();
            while (rs.next()) {
                Vector data_rows = new Vector();
                data_rows.add(rs.getObject(1));
                data_rows.add(rs.getObject(2));
                data_rows.add(rs.getObject(3));
                data_rows.add(rs.getObject(4));
                dtm.addRow(data_rows);
            }
            tblListTypeOfCourse.setRowSelectionInterval(0, 0);
            if(dtm.getRowCount() > 0){
                btnXuatDS.setEnabled(true);
            }else {//chưa có loại môn học nào thì tự động thêm 2 loại môn học mặc định
                tcBLL.InsertTypeOfCourse();
            }
        } catch (Exception ex) {
            Logger.getLogger(FrmTypeOfCourse.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void bindingData(int row) throws ParseException {
        try {
            if (row < 0) {
                row = 0;
            }
            txtIdCourseType.setText(String.valueOf(dtm.getValueAt(row, 0)));
            txtNameOfType.setText(String.valueOf(dtm.getValueAt(row, 1)));
            txtMoneyPerCredit.setText(String.valueOf(dtm.getValueAt(row, 2)).split("\\.")[0]);
            spLessonPerCredit.setValue((int) dtm.getValueAt(row, 3));
        } catch (Exception e) {
            Logger.getLogger(ThongTinSV.class.getName()).log(Level.SEVERE, null, e);
        }
    }

