    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        //kiem tra input
        if (txtIdCourseType.getText().isEmpty()
                || txtNameOfType.getText().isEmpty()
                || txtMoneyPerCredit.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập đủ thông tin");
            return;
        }
        if (BigDecimal.ZERO.compareTo(BigDecimal.valueOf(Long.valueOf(txtMoneyPerCredit.getText()))) > 0
                || (BigDecimal.valueOf(Long.valueOf(txtMoneyPerCredit.getText()))).compareTo(new BigDecimal(99000000)) > 0) {
            JOptionPane.showMessageDialog(this, "Số tiền trên tín chỉ phải trong khoảng 0 - 99000000");
            return;
        }
        //tao doi tuong public
        ClsTypeOfCourse_Public p = new ClsTypeOfCourse_Public();
        p.setMaLoaiMH(txtIdCourseType.getText());
        p.setTenLoai(txtNameOfType.getText());
        p.setSoTienTrenTC(BigDecimal.valueOf(Long.valueOf(txtMoneyPerCredit.getText())));
        p.setSoTietTrenTC((int) spLessonPerCredit.getValue());
        try {
            if (tcBLL.UpdateTypeOfCourse(p) != 0) {
                JOptionPane.showMessageDialog(this, "Cập nhật thành công");
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
                return;
            }
        } catch (Exception ex) {
            Logger.getLogger(FrmTypeOfCourse.class.getName()).log(Level.SEVERE, null, ex);
        }

        //disable cac component
        txtNameOfType.setEditable(false);
        txtMoneyPerCredit.setEditable(false);
        spLessonPerCredit.setEnabled(false);

        //thay doi trang thai btn
        btnCapNhat.setEnabled(true);
        btnLuu.setEnabled(false);
        btnLamMoi.setEnabled(false);
        btnXuatDS.setEnabled(true);
        btnHuyThaoTac.setEnabled(false);
        tblListTypeOfCourse.setEnabled(true);
        //làm mới lại dữ liệu
        LoadData();
    }//GEN-LAST:event_btnLuuActionPerformed

