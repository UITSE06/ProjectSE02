    private void LayDuLieu() {
        try {
            ResultSet rs = kBLL.LayTTKhoa();
            String[] titile = {"Mã khoa", "Tên khoa", "Viết tắt"};
            dtm = new DefaultTableModel(titile, 0);

            while (rs.next()) {
                Vector data_rows = new Vector();
                data_rows.add(rs.getObject(1));
                data_rows.add(rs.getObject(2));
                data_rows.add(rs.getObject(3));
                dtm.addRow(data_rows);
            }
            tblDSKhoa.setModel(dtm);
        } catch (Exception ex) {
            Logger.getLogger(FrmListFaculty.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void bindingData(int row) throws ParseException {
        try {
            if (row < 0) {
                row = 0;
            }
            txtMaKhoa.setText(String.valueOf(dtm.getValueAt(row, 0)));
            txtTenKhoa.setText(String.valueOf(dtm.getValueAt(row, 1)));
            txtVietTat.setText(String.valueOf(dtm.getValueAt(row, 2)));
        } catch (Exception e) {
            Logger.getLogger(FrmStudentManager.class.getName()).log(Level.SEVERE, null, e);
        }
    }

