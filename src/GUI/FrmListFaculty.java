    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        //kiem tra input
        if (txtMaKhoa.getText().isEmpty() || txtTenKhoa.getText().isEmpty() || txtVietTat.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập đủ thông tin");
            return;
        }
        //tao doi tuong public
        clsFaculty_Public p = new clsFaculty_Public();
        p.setIdFaculty(txtMaKhoa.getText());
        p.setNameOfFaculty(txtTenKhoa.getText());
        p.setShortcutName(txtVietTat.getText());
        try {
            if (themMoi)//nếu thêm mới thì gọi hàm thêm
            {
                //goi den ham luu cua BLL
                if (kBLL.ThemKhoa(p) != 0) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }
            } else //không thêm mới tức là cập nhật
            {
                if (kBLL.CapNhatKhoa(p) != 0) {
                    JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(FrmListFaculty.class.getName()).log(Level.SEVERE, null, ex);
        }

        //disable cac component
        txtTenKhoa.setEnabled(false);
        txtVietTat.setEnabled(false);
        //thay doi trang thai btn
        btnHuyThaoTac.setEnabled(false);
        btnThem.setEnabled(true);
        btnCapNhat.setEnabled(true);
        btnLuu.setEnabled(false);
        btnLamMoi.setEnabled(false);
        btnXoa.setEnabled(true);
        btnXuatDS.setEnabled(true);
        //làm mới lại dữ liệu
        LayDuLieu();
    }//GEN-LAST:event_btnLuuActionPerformed

