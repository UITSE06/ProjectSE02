    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        //kiem tra input
        if (spMaxCredit.getValue() == null
                || spMaxScore.getValue() == null
                || spMaxStaff.getValue() == null
                || spMinScore.getValue() == null
                || spMinStaff.getValue() == null
                || spMinStudent.getValue() == null
                || spPassScore.getValue() == null) {
            JOptionPane.showMessageDialog(this, "Chưa nhập đủ thông tin");
            return;
        }
        //kiem tra rang buoc tuoi, diem 
        if((float)spMinScore.getValue() > (float)spPassScore.getValue()){
            JOptionPane.showMessageDialog(this, "Điểm tối thiểu phải nhỏ hơn hoặc bằng điểm đạt");
            return;
        }
        if((float)spMaxScore.getValue() < (float)spPassScore.getValue()){
            JOptionPane.showMessageDialog(this, "Điểm tối đa phải lớn hơn hoặc bằng điểm đạt");
            return;
        }
        if((int)spMinStaff.getValue() > (int)spMaxStaff.getValue()){
            JOptionPane.showMessageDialog(this, "Tuổi nhân viên tối thiểu phải nhỏ hơn tuổi nhân viên tối đa");
            return;
        }
        //tao doi tuong public
        ClsRegulation_Public p = new ClsRegulation_Public();
        p.setPassScore((float) spPassScore.getValue());
        p.setMaxScore((float) spMaxScore.getValue());
        p.setMinScore((float) spMinScore.getValue());
        p.setMinStaffYearOld((int) spMinStaff.getValue());
        p.setMinStudentYearOld((int) spMinStudent.getValue());
        p.setMaxStaffYearOld((int) spMaxStaff.getValue());
        p.setMaxCredit((int) spMaxCredit.getValue());
        try {
            if (reBLL.UpdateRegulation(p) != 0) {
                JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                LoadNewStatic();
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
            }
        } catch (Exception ex) {
            Logger.getLogger(FrmRegulation.class.getName()).log(Level.SEVERE, null, ex);
        }

        //disable cac component
        spMaxCredit.setEnabled(false);
        spMaxScore.setEnabled(false);
        spMaxStaff.setEnabled(false);
        spMinScore.setEnabled(false);
        spMinStudent.setEnabled(false);
        spMinStaff.setEnabled(false);
        spPassScore.setEnabled(false);
        //thay doi trang thai btn
        btnCapNhat.setEnabled(true);
        btnLuu.setEnabled(false);
        btnLamMoi.setEnabled(false);
        btnHuyCapNhat.setEnabled(false);
        //làm mới lại dữ liệu
        LayDuLieu();
    }//GEN-LAST:event_btnLuuActionPerformed

    private void LoadNewStatic() throws Exception {
        //load cac quy dinh len 1 class Static de kiem tra
        ClsRegulationBLL reBLL = new ClsRegulationBLL();
        ClsRegulationStatic_Public rsPulic = new ClsRegulationStatic_Public();
        ResultSet rs = reBLL.LoadRegulation();
        if (rs.next()) {
            rsPulic.setMaxCredit(rs.getInt(2));
            rs.next();
            rsPulic.setMinScore(rs.getFloat(2));
            rs.next();
            rsPulic.setMaxScore(rs.getFloat(2));
            rs.next();
            rsPulic.setPassScore(rs.getFloat(2));
            rs.next();
            rsPulic.setMinStaffYearOld(rs.getInt(2));
            rs.next();
            rsPulic.setMaxStaffYearOld(rs.getInt(2));
            rs.next();
            rsPulic.setMinStudentYearOld(rs.getInt(2));
        }
    }

