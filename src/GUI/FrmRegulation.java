    private void LayDuLieu() {
        try {
            rsRegulation = reBLL.LoadRegulation();
            if (rsRegulation.next()) {
                //binding cac gia tri vao textbox
                spMaxCredit.setValue(rsRegulation.getInt(2));
                rsRegulation.next();
                spMinScore.setValue(rsRegulation.getFloat(2));
                rsRegulation.next();
                spMaxScore.setValue(rsRegulation.getFloat(2));
                rsRegulation.next();
                spPassScore.setValue(rsRegulation.getFloat(2));
                rsRegulation.next();
                spMinStaff.setValue(rsRegulation.getInt(2));
                rsRegulation.next();
                spMaxStaff.setValue(rsRegulation.getInt(2));
                rsRegulation.next();
                spMinStudent.setValue(rsRegulation.getInt(2));
            } else {
                //neu khong co tham so nao thi tu dong them tham so rong
                reBLL.InsertRegulation();
                //them cac gia tri 0 vao textbox
                spMaxCredit.setValue(0);
                spMaxScore.setValue(0);
                spMaxStaff.setValue(0);
                spMinScore.setValue(0);
                spMinStudent.setValue(0);
                spMinStaff.setValue(0);
                spPassScore.setValue(0);
            }

        } catch (Exception ex) {
            Logger.getLogger(FrmRegulation.class.getName()).log(Level.SEVERE, null, ex);
        }
    }


