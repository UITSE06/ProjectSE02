    private void LoadData() {
        dtm = (DefaultTableModel) tblListHumanSubjects.getModel();
        //xoa tat ca cac dong hien tai neu co
        if (dtm.getRowCount() > 0) {
            for (int i = dtm.getRowCount() - 1; i >= 0; i--) {
                dtm.removeRow(i);
            }
        }
        try {
            ResultSet rs = hsBLL.LoadHumanSubjects();
            while (rs.next()) {
                Vector data_rows = new Vector();
                data_rows.add(rs.getObject(1));
                data_rows.add(rs.getObject(2));
                data_rows.add(rs.getObject(3));
                dtm.addRow(data_rows);
            }
            tblListHumanSubjects.setRowSelectionInterval(0, 0);
            btnXuatDS.setEnabled(true);
        } catch (Exception ex) {
            Logger.getLogger(FrmHumanSubjects.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void bindingData(int row) throws ParseException {
        try {
            txtIdHumanSubjects.setText(String.valueOf(dtm.getValueAt(row, 0)));
            txtNameOfHSubjects.setText(String.valueOf(dtm.getValueAt(row, 1)));
            spReduceFeeRatio.setValue((int) dtm.getValueAt(row, 2));
        } catch (Exception e) {
            Logger.getLogger(ThongTinSV.class.getName()).log(Level.SEVERE, null, e);
        }
    }

