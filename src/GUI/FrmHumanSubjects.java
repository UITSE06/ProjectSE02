    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        //kiem tra input
        if (txtNameOfHSubjects.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Chưa nhập đủ thông tin");
            return;
        }
        //tao doi tuong public
        hsPublic.setMaDoiTuong(txtIdHumanSubjects.getText());
        hsPublic.setTenDoiTuong(txtNameOfHSubjects.getText());
        hsPublic.setTiLeGiamHP((int) spReduceFeeRatio.getValue());

        try {
            if (themMoi)//nếu thêm mới thì gọi hàm thêm
            {
                //goi den ham luu cua BLL
                if (hsBLL.InsertHumanSubjects(hsPublic) != 0) {
                    JOptionPane.showMessageDialog(this, "Thêm thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Thêm thất bại");
                }
            } else //không thêm mới tức là cập nhật
            {
                if (hsBLL.UpdateHumanSubjects(hsPublic) != 0) {
                    JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                } else {
                    JOptionPane.showMessageDialog(this, "Cập nhật thất bại");
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(FrmHumanSubjects.class.getName()).log(Level.SEVERE, null, ex);
        }

        //disable cac component
        txtNameOfHSubjects.setEditable(false);
        spReduceFeeRatio.setEnabled(false);
        //thay doi trang thai btn
        btnThem.setEnabled(true);
        btnCapNhat.setEnabled(true);
        btnLuu.setEnabled(false);
        btnLamMoi.setEnabled(false);
        btnXoa.setEnabled(true);
        btnXuatDS.setEnabled(true);
        tblListHumanSubjects.setEnabled(true);
        //làm mới lại dữ liệu
        LoadData();
    }//GEN-LAST:event_btnLuuActionPerformed

